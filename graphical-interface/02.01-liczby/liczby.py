# zastąpić odpowiednią funkcją wczytującą dane od użytkownika
l = input ("podaj liczbę całkowitą: ")

# Wypisze wprowadzoną liczbę
print ("twoja liczba to: ", l)
# Wypisze podwojoną
print ("twoja liczba po podwojeniu to: ", int(l)*2)
# Wypisze wiele razy
print("powtarzając twoją liczbę 5 razy otrzymamy: ", 5*l)
# Wypisze podzieloną przez 3
print ("podzielona przez 3: ", int(l)/3)
# Wypisze resztę z dzielenia
print ("reszta z dzielenia przez 7 to: ", int(l)%7)